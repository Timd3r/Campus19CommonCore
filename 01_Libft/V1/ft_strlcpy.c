int	ft_strlcpy(char *dest, char *src, int i)
{
	while (i > 0 && *dest)
	{
		*dest = *src;
		dest++;
		src++;
		i--;
	}
	i = 0;
	while (src[i] != '\0')
	{
		i++;
	}
	return (i);
}
